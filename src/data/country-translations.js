// Function to get country name in a specific language
export function getCountryName(countrySlug, langCode) {
  const country =
    countries.find((country) => country.slug === countrySlug) ||
    countries.find((country) => country.code === countrySlug?.toUpperCase());
  if (!country) {
    return null;
  }
  // Check if the translation exists for the given language code
  if (country.translations[langCode]) {
    return country.translations[langCode];
  }
  // Fallback to the default English name if translation is not found
  return country.name;
}
